#[allow(dead_code)]
fn main() {
    let stdin = stdin();
    let mut reader = StdinReader::new(stdin.lock());
    let (n, k) = reader.u2();
    let a = reader.uv(n);

    let mut cur = a;
    for _ in 0..k {
        let mut new = vec![1; n];
        //  dbg!(&cur, &new);
        let mut reach = HashMap::new();
        let mut count = 0;
        //左側
        for i in 0..n {
            let k = cur[i];

            //  dbg!(&n, &i, &(n - i + 1), &k, &(k + n - i - 1), &count, &reach);
            new[i] += count;
            match reach.get(&i) {
                Some(&i) => {
                    count -= i;
                }
                None => {}
            }
            reach.remove(&i);
            if k > 0 {
                *reach.entry(k + i).or_insert(0) += 1;
                count += 1;
            }
        }
        //dbg!(&new);
        //右側
        let mut reach = HashMap::new();
        let mut count = 0;
        for i in 0..n {
            let j = n - i - 1;
            let k = cur[j];

            //  dbg!(&n, &i, &(n - i + 1), &k, &(k + n - i - 1));
            new[j] += count;
            match reach.get(&i) {
                Some(&i) => {
                    count -= i;
                }
                None => {}
            }
            reach.remove(&i);
            //   dbg!(&count, &reach);
            if k > 0 {
                *reach.entry(k + i).or_insert(0) += 1;
                count += 1;
            }
        }
        cur = new;
        //   dbg!("-------------------------------------------");
        let mut is_max = true;
        for i in 0..n {
            if cur[i] < n {
                is_max = false;
                break;
            }
        }
        if is_max {
            break;
        }
    }

    for i in 0..n - 1 {
        print!("{} ", cur[i]);
    }
    println!("{}", cur[n - 1]);
}

