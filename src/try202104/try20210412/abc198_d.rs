pub fn solve<R: BufRead>(mut reader: Reader<R>) {
    let s1 = reader.s();
    let s2 = reader.s();
    let s3 = reader.s();

    let mut cs = HashSet::new();
    for &c in &s1 {
        cs.insert(c);
    }
    for &c in &s2 {
        cs.insert(c);
    }
    for &c in &s3 {
        cs.insert(c);
    }

    if cs.len() > 10 {
        println!("UNSOLVABLE");
        return;
    }

    let i_to_c = cs.into_iter().collect_vec();
    let c_to_i = i_to_c
        .iter()
        .cloned()
        .zip(0usize..)
        .collect::<HashMap<_, _>>();

    let v1 = s1.iter().map(|c| *c_to_i.get(c).unwrap()).collect_vec();
    let v2 = s2.iter().map(|c| *c_to_i.get(c).unwrap()).collect_vec();
    let v3 = s3.iter().map(|c| *c_to_i.get(c).unwrap()).collect_vec();

    for pattern in (0..10usize).permutations(10) {
        if pattern[v1[0]] == 0 || pattern[v2[0]] == 0 || pattern[v3[0]] == 0 {
            continue;
        }
        let mut l = 1;
        let mut n1 = 0;
        let mut n2 = 0;
        let mut n3 = 0;
        for i in 0..10 {
            if v1.len() > i {
                n1 += pattern[v1[v1.len() - 1 - i]] * l;
            }
            if v2.len() > i {
                n2 += pattern[v2[v2.len() - 1 - i]] * l;
            }
            if v3.len() > i {
                n3 += pattern[v3[v3.len() - 1 - i]] * l;
            }
            l *= 10;
        }

        if n1 + n2 == n3 {
            println!("{}", n1);
            println!("{}", n2);
            println!("{}", n3);
            return;
        }
    }
    println!("UNSOLVABLE");
}

